"""
Програма має запитувати натуральне число
та вивести в один рядок відповідну цьому числу 
кількість випадкових цілих чисел у діапазоні
від -5 до 7
"""

x = int(input("Введи натуральне число: "))
mi = -5
ma = 7
k = 0
while k <= x :
    k += 1
    print(randint(mi, ma), sep = " ")
print("Кінець програми")